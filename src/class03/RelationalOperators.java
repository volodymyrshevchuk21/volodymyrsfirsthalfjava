package class03;

public class RelationalOperators {
    public static void main(String[] args) {
        int a = 10, b = 11;
        System.out.println(a > b);//
        System.out.println(a < b);
        System.out.println(a >= b);
        System.out.println(a <= b);
        System.out.println(a != b);
        System.out.println(a == b);
    }
}